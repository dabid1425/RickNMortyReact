class CharacterModel {
    constructor(id, name, image, species, status, gender, origin, location, episode, url, created) {
      this.id = id;
      this.name = name;
      this.image = image;
      this.species = species;
      this.status = status;
      this.gender = gender;
      this.origin = origin;
      this.location = location;
      this.episode = episode;
      this.url = url
      this.created = created;
    }
  }
  
  export default CharacterModel;
  